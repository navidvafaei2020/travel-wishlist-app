package lu.travelwishlist.app.entity;

/**
 * Enum representing the status of a user account.
 *
 * <p>
 * - ACTIVE: The user account is active and can perform actions.
 * - DISABLED: The user account is inactive and cannot perform actions.
 */
public enum UserStatus {
    ACTIVE,    // user status is active
    DISABLED   // User status is not Active
}